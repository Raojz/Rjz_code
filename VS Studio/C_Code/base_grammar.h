/*******************             一、C语言执行流程            *******************************
 一、编写源代码
	编写源代码是C语言程序执行的起始步骤，开发者需要使用文本编辑器（如VS Code、Vim、Notepad++等）
	编写符合C语言语法规则的代码，最终生成以.c为后缀的源文件。
 二、预处理
	预处理阶段由C预处理器（如GCC的cpp、MSVC的预处理器）完成，
	主要处理源代码中以#开头的预处理指令，最终生成以.i为后缀的预处理文件。
 三、编译
	编译阶段由编译器（如GCC、Clang、MSVC等）完成，将预处理后的.i文件进行词法分析、语法分析、
	语义分析、代码优化，最终转换为特定平台的汇编代码，生成以.s为后缀的汇编文件
四、汇编
	汇编阶段由汇编器（如GCC的as、NASM等）完成，将汇编代码翻译成机器指令（二进制目标代码），
	生成以.o（Linux 系统）或.obj（Windows 系统）为后缀的目标文件。
五、链接
	链接阶段由链接器（如GCC的ld、VS的link.exe等）完成，将多个目标文件以及所需的库文件（如C标准库）组合
	成一个完整的、可被操作系统加载执行的可执行文件，在Linux系统中后缀为.out，Windows系统中后缀为.exe。
六、加载运行
	当生成可执行文件后，需要将其加载到内存中才能运行，这个过程由操作系统的加载器完成。
	加载完成后，操作系统将控制权交给程序，CPU开始执行内存中的机器指令，程序正式运行。
**********************************************************************************************/


/***************************       二、标识符和关键字                  ****************************
1、标识符用于给变量、函数、类型等命名，以便在程序中进行区分和引用，其构成规则如下：
- 由字母（A - Z，a - z）、数字（0 - 9）和下划线（_）组成
- 首字符不能是数字
- 不能是C语言中的关键字（关键字在C语言中有特定的语法功能，不能用作普通命名）
- 标识符区分大小写，例如myVar和MyVar是两个不同的标识符

2、关键字是C语言中有特殊含义或功能的单词(只能小写)，常见的关键字及含义如下：
register—>(将变量存在cpu寄存器内,适合需要频繁读写的变量)提高变量访问速度，优化代码执行效率，修饰局部变量或形参时不能进行取地址操作；
static—>限制函数或全局变量作用域，延长局部变量生命周期，变量赋值初始化后会保留上次的值；
volatile—>告诉编译器该变量可能被外部因素意外修改，防止编译器优化，确保每次读到变量正确的值；
const—>定义只读变量或常量，确保变量值不可改；
*******************************************************************************************************/


