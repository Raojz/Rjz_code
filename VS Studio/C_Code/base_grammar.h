/***************************       六、三种基本控制结构           ****************************
顺序结构：按照代码的书写顺序依次执行。
选择结构：根据条件判断来决定执行哪一段代码，常见的有if语句和switch语句。
循环结构：重复执行某段代码直到满足某个条件为止，常见的有for循环、while循环和do - while循环。
在C语言中，break和continue是两个用于控制循环流程的关键字。它们的作用如下：
break: 当break语句出现在循环体内时，它会立即终止整个循环，跳出循环体，继续执行循环后面的代码。
continue: 当continue语句出现在循环体内时，它会跳过本次循环剩余的代码，并开始下一次循环迭代。
**********************************************************************************************/












