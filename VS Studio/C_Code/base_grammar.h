/***************************       七、函数的基本概念          ****************************
一、什么是函数？
函数（Function）是执行特定任务的独立代码块。你可以把它想象成一个“黑盒子”，你给它一些输入（参数），它根据内部定义的步骤进行处理，然后给你一个输出（返回值）或者完成某种操作。
在C语言中，函数是程序的基本构建块，整个C程序就是由一个或多个函数组成的。main函数是每个C程序的入口点。
函数的优点；
1.模块化 (Modularity): 将复杂的程序分解为更小、更易于管理的部分（函数）。每个函数负责一个具体的任务。
2.可重用性 (Reusability): 一旦定义了函数，就可以在程序的任何地方多次调用它，避免重复编写相同的代码。
3.可维护性 (Maintainability): 当需要修改某个功能时，只需要修改对应的函数即可，不影响其他部分的代码。
4.可读性 (Readability): 通过给函数起有意义的名字，可以让主程序的逻辑更清晰易懂。

二、函数的定义格式为：
返回类型 函数名(参数列表) {
	// 函数体
	return 返回值;
}
- 返回类型：函数返回值的类型，如果没有返回值，使用void。
- 函数名：函数的名称，遵循标识符的命名规则。
- 参数列表：函数的参数，由数据类型和参数名组成，多个参数之间用逗号分隔，如果没有参数，参数列表可以为空或写void。
- 函数体：函数的具体实现代码。
- return 语句：用于返回函数的返回值，当返回类型为void时，可以省略 return 语句。

三、函数的声明：
函数声明告诉编译器函数的名称、返回值类型以及参数列表。它并不包含函数的实际实现（函数体）。
声明通常放在函数被调用之前，最常见的是放在源文件的开头或头文件中。

四、形参与实参
形参 (Formal Parameter): 在函数定义时，括号内的变量名。它们是占位符，在函数未被调用时不占用内存。
实参 (Actual Parameter): 在函数调用时，传递给函数的具体值或变量。
重要概念：传值调用
在C语言中，函数参数的传递默认是“传值”的。
这意味着当调用函数时，实参的值会被复制一份给形参。在函数内部对形参的任何修改，都不会影响到原始的实参变量。
**********************************************************************************************/

